$(function() {
	var brands = {};
	var models = {};
	var subcategories = {};

	$.each(categories, function() {
		$("<option />").attr("value", this.value).html(this.name).appendTo(
				"#category");
		brands[this.value] = this.brands;
		subcategories[this.value] = this.subcategories;
	});
	
	$("#category").change(
			function() {
				var value = $(this).val();
				var brandField = $("#brand");
				var subCategoryField = $("#subcategory");

				subCategoryField.empty();

				$.each(subcategories[value], function() {
					$("<option />").attr("value", this.value).html(this.name).appendTo("#subcategory");
				});

				brandField.empty();

				$.each(brands[value], function() {
					$("<option />").attr("value", this.value).html(this.name).appendTo("#brand");
					models[this.value] = this.models;
				});

				$("#brand").change(
						function() {
							var value = $(this).val();
							var modelField = $("#model");

							modelField.empty();

							$.each(models[value], function() {
								$("<option/>").attr("value", this.value).html(this.name).appendTo("#model");
							})
						}).change();
			}).change();
});